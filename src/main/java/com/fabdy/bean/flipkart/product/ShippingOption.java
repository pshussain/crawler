package com.fabdy.bean.flipkart.product;

public class ShippingOption {
	long estimatedDelivery;
	String deliveryTimeUnits;
	String shippingType;
	/**
	 * @return the estimatedDelivery
	 */
	public long getEstimatedDelivery() {
		return estimatedDelivery;
	}
	/**
	 * @return the deliveryTimeUnits
	 */
	public String getDeliveryTimeUnits() {
		return deliveryTimeUnits;
	}
	/**
	 * @return the shippingType
	 */
	public String getShippingType() {
		return shippingType;
	}
}
